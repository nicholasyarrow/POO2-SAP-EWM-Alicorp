private void registrar() throws Exception {

        String nombres = txtNombreUsuario.getText();
        String apellidos = txtApellidos.getText();
        String usuario = txtNombreUsuario.getText();
        String clave = txtClave.getText();
        String rol = cbxTipoUsuario.getSelectedItem().toString();

        if (nombres.equals("") || apellidos.equals("") || usuario.equals("") || clave.equals("") || rol.equals("")) {
            JOptionPane.showMessageDialog(null, "Todos los campos son requeridos");
        } else {
            usuariodto.setNombres(nombres);
            usuariodto.setApellidos(apellidos);
            usuariodto.setUsuario(usuario);
            usuariodto.setClave(clave);
            usuariodto.setRol(rol);

            if (usuariodao.registrar(usuariodto)) {
                JOptionPane.showMessageDialog(null, "Usuario Registrado");
                btnGuardar.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(null, "Error al Registrar");
            }
        }

    }
